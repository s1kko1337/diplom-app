function buildSteps(template, overrideFn) {
  return template.map((item, i) => {
    const base = {
      id: `step-${i + 1}`,
      description: item.description,
      requirement: item.requirement,
      tools: item.tools,
      status: 'pending',
      comment: null,
      startedAt: null,
      completedAt: null,
      measurements: item.measurements ? item.measurements.map((m) => ({ ...m })) : [],
      materials: item.materials ? item.materials.map((m) => ({ ...m })) : [],
    }
    return overrideFn ? { ...base, ...overrideFn(i, base) } : base
  })
}

